package com.google.maps.response;

/**
 * @author NGoyal
 *
 */
public class ShopDetails {

	private String shopName;
	private String shopAddress;
	private String shopPostalCode;
	private String latitude;
	private String longitude;
	/**
	 * @return the shopName
	 */
	public String getShopName() {
		return shopName;
	}
	/**
	 * @param shopName the shopName to set
	 */
	public void setShopName(String shopName) {
		this.shopName = shopName;
	}
	/**
	 * @return the shopAddress
	 */
	public String getShopAddress() {
		return shopAddress;
	}
	/**
	 * @param shopAddress the shopAddress to set
	 */
	public void setShopAddress(String shopAddress) {
		this.shopAddress = shopAddress;
	}
	/**
	 * @return the shopPostalCode
	 */
	public String getShopPostalCode() {
		return shopPostalCode;
	}
	/**
	 * @param shopPostalCode the shopPostalCode to set
	 */
	public void setShopPostalCode(String shopPostalCode) {
		this.shopPostalCode = shopPostalCode;
	}
	/**
	 * @return the latitude
	 */
	public String getLatitude() {
		return latitude;
	}
	/**
	 * @param latitude the latitude to set
	 */
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	/**
	 * @return the longitude
	 */
	public String getLongitude() {
		return longitude;
	}
	/**
	 * @param longitude the longitude to set
	 */
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	
	
}
